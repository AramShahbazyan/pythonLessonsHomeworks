# Գտեք բոլոր կատարյալ թվերը մինչև 10000: Կատարյալ թիվն այն թիվն է, որը հավասար է իր բոլոր բաժանարարների գումարին,
# բացի իրենից: Օրինակ՝ 6 թիվը կատարյալ է, քանի որ բացի իրենից բաժանվում է 1, 2 և 3 թվերի վրա, որոնք գումարվում են 6-ի։

i = 1
sum_nums = 0
while i < 10000:
    j = 1
    while j < i:
        if i % j == 0:
            sum_nums += j
        j += 1
    if sum_nums == i:
        print(sum_nums)
    sum_nums = 0
    i += 1
